    private void btn_masukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_masukActionPerformed
        // TODO add your handling code here:
        try{
       Statement statement = (Statement) Koneksi_db.getcoConnection() .createStatement();
       ResultSet result = statement.executeQuery
        ("select * from tblogin where" + " username = '" + txt_user.getText()+ "'");
       if(result.next()){
           if(txt_pass .getText() .equals(result.getString("Password"))){
               new From_Utama().show();
               this.dispose();
           }
           else{
               JOptionPane.showMessageDialog(rootPane, "password salah");
               txt_user.setText("");
               txt_pass.setText("");
               txt_pass.requestFocus();
           }
       }
       else{
           JOptionPane.showMessageDialog(rootPane, "User tidak ditemukan");
           txt_user.setText("");
           txt_pass.setText("");
           txt_user.requestFocus();
       }
   }
   catch(Exception e){
       JOptionPane.showMessageDialog(rootPane, "Koneksi gagal");
   }
    }//GEN-LAST:event_btn_masukActionPerformed

